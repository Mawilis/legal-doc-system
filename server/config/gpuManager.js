class GPUDevice extends EventEmitter {
  constructor(index, info) {
    super();

    this.id = `gpu-${index}`;
    this.index = index;
    this.info = info;
    this.vendor = this.detectVendor(info);
    this.state = GPU_CONSTANTS.STATES.AVAILABLE;
    this.jobs = [];
    this.metrics = {
      utilization: 0,
      memoryTotal: 0,
      memoryUsed: 0,
      memoryFree: 0,
      temperature: 0,
      powerUsage: 0,
      processes: 0,
    };
    this.lastUpdated = Date.now();
    this.errorCount = 0;
    this.totalJobsProcessed = 0;
    this.totalProcessingTime = 0;
  }

  detectVendor(info) {
    const name = (info.name || '').toLowerCase();
    if (name.includes('nvidia')) return GPU_CONSTANTS.VENDORS.NVIDIA;
    if (name.includes('amd') || name.includes('radeon')) return GPU_CONSTANTS.VENDORS.AMD;
    if (name.includes('intel')) return GPU_CONSTANTS.VENDORS.INTEL;
    return GPU_CONSTANTS.VENDORS.CLOUD;
  }

  async updateMetrics(metrics) {
    this.metrics = { ...this.metrics, ...metrics };
    this.lastUpdated = Date.now();

    // Update Prometheus metrics
    gpuMetrics.gpuUtilization.labels(this.id, this.info.name).set(this.metrics.utilization);
    gpuMetrics.gpuMemoryTotal.labels(this.id, this.info.name).set(this.metrics.memoryTotal);
    gpuMetrics.gpuMemoryUsed.labels(this.id, this.info.name).set(this.metrics.memoryUsed);
    gpuMetrics.gpuMemoryFree.labels(this.id, this.info.name).set(this.metrics.memoryFree);
    gpuMetrics.gpuTemperature.labels(this.id, this.info.name).set(this.metrics.temperature);
    gpuMetrics.gpuPowerUsage.labels(this.id, this.info.name).set(this.metrics.powerUsage);
    gpuMetrics.gpuProcesses.labels(this.id).set(this.metrics.processes);

    // Check thresholds
    this.checkThresholds();
  }

  checkThresholds() {
    // Temperature check
    if (this.metrics.temperature > GPU_CONSTANTS.TEMPERATURE_LIMIT) {
      this.emit('warning', {
        type: 'high_temperature',
        gpuId: this.id,
        temperature: this.metrics.temperature,
        limit: GPU_CONSTANTS.TEMPERATURE_LIMIT,
      });

      if (this.metrics.temperature > GPU_CONSTANTS.TEMPERATURE_LIMIT * 1.1) {
        this.state = GPU_CONSTANTS.STATES.ERROR;
        this.emit('error', {
          type: GPU_CONSTANTS.ERRORS.OVERHEAT,
          gpuId: this.id,
          temperature: this.metrics.temperature,
        });
        gpuMetrics.gpuErrorsTotal.labels(this.id, GPU_CONSTANTS.ERRORS.OVERHEAT).inc();
      }
    }

    // Power check
    if (this.metrics.powerUsage > GPU_CONSTANTS.POWER_LIMIT) {
      this.emit('warning', {
        type: 'high_power',
        gpuId: this.id,
        power: this.metrics.powerUsage,
        limit: GPU_CONSTANTS.POWER_LIMIT,
      });
    }

    // Memory check
    const memoryUsageRatio = this.metrics.memoryUsed / this.metrics.memoryTotal;
    if (memoryUsageRatio > 0.95) {
      this.emit('warning', {
        type: 'high_memory_usage',
        gpuId: this.id,
        usage: memoryUsageRatio,
      });
    }
  }

  canAcceptJob(job) {
    if (this.state !== GPU_CONSTANTS.STATES.AVAILABLE) return false;
    if (this.jobs.length >= GPU_CONSTANTS.MAX_JOBS_PER_GPU) return false;

    // Check memory requirements
    const requiredMemory = job.requiredMemory || 0;
    const availableMemory =
      this.metrics.memoryTotal * GPU_CONSTANTS.MEMORY_FRACTION - this.metrics.memoryUsed;

    return availableMemory >= requiredMemory;
  }

  async assignJob(job) {
    if (!this.canAcceptJob(job)) {
      throw new Error(`GPU ${this.id} cannot accept job`);
    }

    this.jobs.push({
      ...job,
      assignedAt: Date.now(),
      startedAt: null,
      completedAt: null,
    });

    if (this.jobs.length >= GPU_CONSTANTS.MAX_JOBS_PER_GPU) {
      this.state = GPU_CONSTANTS.STATES.BUSY;
    }

    this.emit('job_assigned', { gpuId: this.id, jobId: job.id });

    return this.jobs.length - 1;
  }

  startJob(jobIndex) {
    if (jobIndex < 0 || jobIndex >= this.jobs.length) {
      throw new Error(`Invalid job index: ${jobIndex}`);
    }

    this.jobs[jobIndex].startedAt = Date.now();
    this.emit('job_started', {
      gpuId: this.id,
      jobId: this.jobs[jobIndex].id,
    });
  }

  completeJob(jobIndex, result) {
    if (jobIndex < 0 || jobIndex >= this.jobs.length) {
      throw new Error(`Invalid job index: ${jobIndex}`);
    }

    const job = this.jobs[jobIndex];
    job.completedAt = Date.now();
    job.result = result;
    job.duration = job.completedAt - (job.startedAt || job.assignedAt);

    this.totalJobsProcessed++;
    this.totalProcessingTime += job.duration;

    // Remove job
    this.jobs.splice(jobIndex, 1);

    if (this.jobs.length < GPU_CONSTANTS.MAX_JOBS_PER_GPU) {
      this.state = GPU_CONSTANTS.STATES.AVAILABLE;
    }

    this.emit('job_completed', {
      gpuId: this.id,
      jobId: job.id,
      duration: job.duration,
    });

    return job;
  }

  failJob(jobIndex, error) {
    if (jobIndex < 0 || jobIndex >= this.jobs.length) {
      throw new Error(`Invalid job index: ${jobIndex}`);
    }

    const job = this.jobs[jobIndex];
    job.error = error;
    job.failedAt = Date.now();

    this.errorCount++;
    gpuMetrics.gpuErrorsTotal.labels(this.id, error.type || 'unknown').inc();

    // Remove job
    this.jobs.splice(jobIndex, 1);

    if (this.jobs.length < GPU_CONSTANTS.MAX_JOBS_PER_GPU) {
      this.state = GPU_CONSTANTS.STATES.AVAILABLE;
    }

    this.emit('job_failed', {
      gpuId: this.id,
      jobId: job.id,
      error,
    });
  }

  getStats() {
    return {
      id: this.id,
      index: this.index,
      vendor: this.vendor,
      name: this.info.name,
      state: this.state,
      metrics: this.metrics,
      jobs: {
        current: this.jobs.length,
        totalProcessed: this.totalJobsProcessed,
        totalProcessingTime: this.totalProcessingTime,
        averageProcessingTime:
          this.totalJobsProcessed > 0 ? this.totalProcessingTime / this.totalJobsProcessed : 0,
      },
      errors: this.errorCount,
      lastUpdated: this.lastUpdated,
    };
  }

  async healthCheck() {
    const now = Date.now();
    const timeSinceUpdate = now - this.lastUpdated;

    if (timeSinceUpdate > GPU_CONSTANTS.HEALTH_CHECK_INTERVAL * 2) {
      this.state = GPU_CONSTANTS.STATES.OFFLINE;
      return false;
    }

    if (this.state === GPU_CONSTANTS.STATES.ERROR) {
      return false;
    }

    return true;
  }
}

