/**
 * @function sendSovereignDocument
 * @description Transmits the generated PDF buffer to the client with strict institutional headers.
 * Ensures the document is treated as a downloadable artifact (attachment) rather than rendered inline.
 * * @param {import('express').Response} res - The Express response object.
 * @param {Buffer} buffer - The raw binary PDF artifact generated by the pdfService.
 * @param {string} type - The classification of the document (e.g., 'Revenue', 'Compliance', 'Forensics').
 * @param {string} tenantId - The unique sovereign identifier for the active tenant.
 * @returns {void}
 * * @forensic Locks the Content-Disposition and Content-Type headers before transit to prevent MIME-sniffing vulnerabilities.
 */
const sendSovereignDocument = (res, buffer, type, tenantId) => {
  res.setHeader('Content-Type', 'application/pdf');
  res.setHeader('Content-Disposition', `attachment; filename=WILSY_${type.toUpperCase()}_${tenantId}_${Date.now()}.pdf`);
  res.send(buffer);
};

/**
 * @function pickStatementDate
 * @description Normalizes query dates into explicit statement metadata without inventing hidden periods.
 * @param {string|undefined} value - Candidate ISO date from the statement request.
 * @param {string} fallback - Fallback ISO date when the candidate is missing.
 * @returns {string} ISO date string suitable for PDF evidence.
 * @collaboration Wilson Khanyezi required operator-controlled statement dates for investor-grade billing artifacts.
 */
const pickStatementDate = (value, fallback) => (value ? String(value).slice(0, 10) : fallback);

/**
 * @function buildStatementNumber
 * @description Creates a deterministic, readable statement number from tenant and request nonce.
 * @param {string} tenantId - Active tenant identifier.
 * @param {string} nonce - Request nonce or trace id.
 * @returns {string} Boardroom-readable statement number.
 * @collaboration Makes every exported statement traceable without forcing investors to read raw telemetry ids.
 */
const buildStatementNumber = (tenantId, nonce) => `WILSY-REV-${tenantId.toUpperCase()}-${String(nonce).slice(-8).toUpperCase()}`;

/**
 * @function buildRevenueEvidenceRows
 * @description Converts revenue statement metrics into a concise source-of-truth table for the PDF.
 * @param {Object} params - Evidence construction parameters.
 * @param {Object|null} params.revenueData - Latest live revenue row when available.
 * @param {Object} params.metrics - Statement metrics.
 * @param {Object} params.statement - Statement metadata.
 * @param {string} params.sourceStatus - Data source status.
 * @returns {Array<Object>} Evidence rows for the PDF renderer.
 * @collaboration Wilson Khanyezi rejected empty ceremonial statements; every exported artifact must explain where values came from.
 */
const buildRevenueEvidenceRows = ({ revenueData, metrics, statement, sourceStatus }) => ([
  { label: 'Revenue source', value: sourceStatus, note: revenueData?._id ? `DB row ${revenueData._id}` : 'Request context only' },
  { label: 'Billing period', value: `${statement.billingPeriodStart} to ${statement.billingPeriodEnd}`, note: statement.billingModel },
  { label: 'Payment policy', value: `${statement.paymentTerms} / Due ${statement.dueDate}`, note: `Issued ${statement.issueDate}` },
  { label: 'ARR / MRR', value: `R ${Number(metrics.arr || 0).toLocaleString()} / R ${Number(metrics.mrr || 0).toLocaleString()}`, note: metrics.growth },
  { label: 'Document basis', value: statement.description || 'No description supplied', note: statement.clientId || statement.tenantId }
]);

/**
 * @function parseStatementNumber
 * @description Parses numeric query values for statement evidence without inventing non-zero fallbacks.
 * @param {unknown} value - Query value supplied by the client.
 * @param {number|null} fallback - Optional fallback when the value is absent or invalid.
 * @returns {number|null} Parsed finite number or fallback.
 * @collaboration Wilson Khanyezi required artifact numbers to be traceable to request or DB state only.
 */
const parseStatementNumber = (value, fallback = null) => {
  const numeric = Number(value);
  return Number.isFinite(numeric) ? numeric : fallback;
};

/**
 * @function resolveStatementTenantId
 * @description Resolves the statement tenant from query, headers, middleware or founder fallback without losing GLOBAL_ROOT context.
 * @param {import('express').Request} req - Incoming statement request.
 * @returns {string} Lowercase tenant id used for scoped DB and branding operations.
 * @collaboration Keeps Wilsy founder exports anchored to the requested tenant instead of silently drifting to middleware defaults.
 */
const resolveStatementTenantId = (req) => String(
  req.query?.tenantId
  || req.headers['x-tenant-id']
  || req.tenant?.id
  || req.user?.tenantId
  || 'GLOBAL_ROOT'
).toLowerCase();

/**
 * @function getScopedStatementModel
 * @description Returns a cached model from a tenant shard, preventing OverwriteModelError under repeated boardroom exports.
 * @param {import('mongoose').Connection} db - Tenant-scoped mongoose connection.
 * @param {string} modelName - Mongoose model name.
 * @param {import('mongoose').Schema} schema - Model schema.
 * @param {string} collectionName - Physical collection name.
 * @returns {import('mongoose').Model} Cached or newly registered model.
 * @collaboration Statement generation must survive rapid investor demos without model registration fractures.
 */
const getScopedStatementModel = (db, modelName, schema, collectionName) => (
  db.models?.[modelName] || db.model(modelName, schema, collectionName)
);

/**
 * @function buildStatementMetadata
 * @description Builds shared statement metadata from request context for all artifact types.
 * @param {import('express').Request} req - Incoming statement request.
 * @param {string} tenantId - Resolved tenant identifier.
 * @param {string} nonce - Request nonce or trace id.
 * @param {string} hud - Active HUD/export mode.
 * @returns {Object} Normalized statement metadata.
 * @collaboration Ensures revenue, compliance and forensic exports carry the same dates, terms and operator-selected document basis.
 */
const buildStatementMetadata = (req, tenantId, nonce, hud) => {
  const timestamp = new Date().toISOString();
  const today = timestamp.slice(0, 10);
  return {
    statementNumber: buildStatementNumber(tenantId, nonce),
    tenantId,
    clientId: String(req.query?.clientId || 'CLIENT-ROOT'),
    issueDate: pickStatementDate(req.query?.issueDate || req.query?.statementDate, today),
    statementDate: pickStatementDate(req.query?.statementDate || req.query?.issueDate, today),
    dueDate: pickStatementDate(req.query?.dueDate, today),
    billingPeriodStart: pickStatementDate(req.query?.billingPeriodStart, today),
    billingPeriodEnd: pickStatementDate(req.query?.billingPeriodEnd, today),
    paymentTerms: String(req.query?.paymentTerms || 'NET_30'),
    billingModel: String(req.query?.billingModel || 'PLATFORM_RETAINER'),
    invoiceClass: String(req.query?.invoiceClass || hud),
    taxType: String(req.query?.taxType || 'VAT'),
    taxJurisdiction: String(req.query?.taxJurisdiction || 'ZA'),
    description: String(req.query?.description || 'Wilsy OS sovereign operating services'),
    amount: parseStatementNumber(req.query?.amount, 0),
    sourceStatus: String(req.query?.operationsStatus || 'REQUEST_CONTEXT_FALLBACK')
  };
};

/**
 * @function buildStatementOperations
 * @description Extracts live operation coverage from the Revenue Ledger statement request.
 * @param {import('express').Request} req - Incoming statement request.
 * @returns {Object} Operation coverage for the PDF evidence layer.
 * @collaboration Turns the artifact into a source map: investors can see what was live, silent, or DB-backed.
 */
const buildStatementOperations = (req) => ({
  status: String(req.query?.operationsStatus || 'SOURCE_UNDECLARED'),
  liveSources: parseStatementNumber(req.query?.liveSourceCount, 0),
  totalSources: parseStatementNumber(req.query?.totalSourceCount, 0),
  silentSources: parseStatementNumber(req.query?.silentSourceCount, 0),
  invoiceRows: parseStatementNumber(req.query?.invoiceRows, null),
  openReceivables: parseStatementNumber(req.query?.openReceivables, 0),
  pendingPayments: parseStatementNumber(req.query?.pendingPayments, 0),
  dso: parseStatementNumber(req.query?.dso, null),
  decisionCount: parseStatementNumber(req.query?.decisionCount, 0),
  lastSync: req.query?.lastOperationSync ? String(req.query.lastOperationSync) : null
});

/**
 * @function generateRevenueStatement
 * @description Generates a branded, cryptographically sealed Revenue Statement for the active tenant.
 * Fetches live financial metrics from the dynamic tenant shard and logs the generation event to the forensic ledger.
 * * @param {import('express').Request} req - The incoming Express request, containing the tenant context and trace headers.
 * @param {import('express').Response} res - The outgoing Express response, used to stream the sealed artifact.
 * @param {import('express').NextFunction} next - The Express next middleware function, used to pass failures to the global error handler.
 * @returns {Promise<void>}
 * * @real-world Used by executives and shareholders to export verifiable, investor-grade financial snapshots in real-time.
 * @forensic Creates an immutable ForensicLog entry anchored with a deterministic SHA3-512 payload seal to prevent retroactive tampering.
 */
export const generateRevenueStatement = async (req, res, next) => {
  const tenantId = resolveStatementTenantId(req);
  try {
    const queryArr = Number(req.query?.arr || 0);
    const queryGrowth = Number(req.query?.growth || 0);
    const hud = String(req.query?.hud || 'REVENUE').toUpperCase();
    let revenueData = null;
    let ForensicLog = null;

    try {
      const db = getSovereignDb().useDb(tenantId, { useCache: true });
      // 🔧 EXTRACT SCHEMA: Use .schema to build isolated tenant models dynamically
      const Revenue = getScopedStatementModel(db, 'Revenue', GlobalRevenueModel.schema, 'revenues');
      ForensicLog = getScopedStatementModel(db, 'ForensicLog', GlobalForensicLogModel.schema, 'forensiclogs');

      // 🏛️ REVENUE NUCLEUS STRIKE: Fetching the most recent institutional financial state
      revenueData = await Revenue.findOne({ tenantId }).sort({ timestamp: -1 });
    } catch (dbError) {
      console.warn('[STATEMENT_WARN] Revenue shard fallback:', dbError.message);
    }

    const nonce = req.headers['x-request-id'] || `AUTO-${Date.now()}`;
    const timestamp = new Date().toISOString();
    const sourceStatus = revenueData ? 'LIVE_REVENUE_DB' : 'REQUEST_CONTEXT_FALLBACK';
    const statement = {
      ...buildStatementMetadata(req, tenantId, nonce, hud),
      amount: Number(req.query?.amount || revenueData?.amount || queryArr || 0),
      sourceStatus
    };
    const operations = buildStatementOperations(req);
    const statementAmount = Number(revenueData?.amount || statement.amount || queryArr || 0);

    const data = {
      tenantId,
      branding: {
        ...brandingConfig,
        headerTitle: brandingConfig.headers.revenue
      },
      statement,
      metrics: {
        arr: statementAmount.toFixed(2),
        arrDelta: revenueData?.metadata?.arrDelta ? `${revenueData.metadata.arrDelta.toFixed(2)}%` : `${queryGrowth.toFixed(2)}%`,
        mrr: (statementAmount / 12).toFixed(2),
        growth: revenueData?.metadata?.growth ? `${revenueData.metadata.growth.toFixed(2)}%` : `${queryGrowth.toFixed(2)}%`,
        hud,
        amount: statement.amount.toFixed(2)
      },
      operations,
      evidence: [],
      forensic: { note: 'Institutional Revenue Singularity Strike', nonce, timestamp, sourceStatus }
    };
    data.evidence = buildRevenueEvidenceRows({
      revenueData,
      metrics: data.metrics,
      statement,
      sourceStatus
    });

    // 🛡️ FORENSIC ANCHORING: Generate seal for this specific document
    const seal = generateDeterministicSeal(data, nonce, timestamp);

    if (ForensicLog) {
      await ForensicLog.create({
        eventType: 'REVENUE_STATEMENT_GENERATION',
        category: 'REVENUE',
        performedBy: req.user?._id || 'SYSTEM_CORE',
        tenantId,
        status: 'SUCCESS',
        eventSeal: seal,
        metadata: { requestId: nonce }
      }).catch(logError => {
        console.warn('[STATEMENT_WARN] Revenue forensic log deferred:', logError.message);
      });
    }

    const pdfBuffer = await pdfService.generateInstitutionalPDF('revenue', data, tenantId);
    sendSovereignDocument(res, pdfBuffer, 'Revenue', tenantId);
    broadcastTelemetry(tenantId, 'STATEMENT_ENGINE', 'REVENUE_EXPORT_SUCCESS', 'RevenueStatement', { seal });
  } catch (err) {
    console.error('[STATEMENT_ERROR] Revenue fracture:', err);
    if (typeof next === 'function') next(err);
    else res.status(500).json({ error: 'Revenue Statement Generation Failed', details: err.message });
  }
};

