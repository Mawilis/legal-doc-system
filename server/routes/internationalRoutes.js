/*
 * @route   GET /api/v1/international/profile/:code
 * @desc    Get detailed profile for a specific jurisdiction
 * @access  Premium
 * @tier    PREMIUM
 */
router.get(
  '/profile/:code',
  param('code').isLength({ min: 2, max: 2 }).isAlpha(),
  tieredRateLimiter('premium'),
  cacheMiddleware({ ttl: CACHE_TTL.PROFILE }),
  async (req, res, next) => {
    const startTime = performance.now();
    const correlationId = req.correlationId;
    const { id: tenantId } = req.tenantContext;
    const userId = req.user?.id;
    const { code } = req.params;

    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({ errors: errors.array() });
    }

    try {
      const analyzer = getAnalyzer();
      const profile = analyzer.getJurisdictionProfile(code.toUpperCase());

      if (!profile) {
        throw new AppError('Jurisdiction not found', 404, 'JURISDICTION_NOT_FOUND');
      }

      // Track billing
      const charge = await trackBilling(
        tenantId,
        userId,
        'get_profile',
        INTERNATIONAL_TIERS.PREMIUM,
        {
          jurisdiction: code,
        }
      );

      await QuantumLogger.logAction(tenantId, userId, 'INTL_PROFILE_VIEW', null, {
        jurisdiction: code,
        legalSystem: profile.legalSystem,
        charge,
        tier: INTERNATIONAL_TIERS.PREMIUM,
      });

      const duration = performance.now() - startTime;
      metrics.timing('international.profile.duration', duration);

      res.json(
        formatSuccess(profile, {
          self: req.originalUrl,
          processingTimeMs: Math.round(duration),
          correlationId,
          jurisdiction: code,
          tier: INTERNATIONAL_TIERS.PREMIUM,
          charge,
        })
      );
    } catch (error) {
      trackError('international', error.code || 'profile_error');
      next(error);
    }
  }
);

