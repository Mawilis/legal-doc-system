/*
 * Generate correlation ID for request tracking
 */
const generateCorrelationId = (req) => {
  return (
    req.headers['x-correlation-id'] ||
    req.headers['x-request-id'] ||
    `INTL-${Date.now()}-${uuidv4().substring(0, 8)}`
  );
};

/*
 * Calculate premium charge based on tier
 */
const calculatePremiumCharge = (tier = INTERNATIONAL_TIERS.PREMIUM) => {
  const multiplier = PRICING_MULTIPLIERS[tier] || 1;
  return BASE_QUERY_PRICE * multiplier;
};

/*
 * Track billing for premium endpoints
 */
const trackBilling = async (tenantId, userId, endpoint, tier, metadata = {}) => {
  const charge = calculatePremiumCharge(tier);

  await BillingService.trackUsage({
    tenantId,
    userId,
    feature: `international_${endpoint}`,
    tier,
    charge,
    metadata: {
      ...metadata,
      premiumMultiplier: PRICING_MULTIPLIERS[tier],
      basePrice: BASE_QUERY_PRICE,
    },
  });

  // Update metrics
  metrics.increment('international.billing.charge', { tier, endpoint }, charge);

  return charge;
};

/*
 * Get analyzer instance
 */
const getAnalyzer = () => {
  return CrossJurisdictionAnalyzerFactory.getAnalyzer();
};

/*
 * Format success response
 */
const formatSuccess = (data, metadata = {}) => {
  return {
    success: true,
    data,
    metadata: {
      timestamp: new Date().toISOString(),
      version: '42.0.0',
      ...metadata,
    },
    links: {
      self: metadata.self,
      docs: 'https://docs.wilsy.os/api/international',
    },
  };
};

/*
 * Format error response
 */
const formatError = (error, correlationId) => {
  return {
    success: false,
    error: {
      code: error.code || 'INTERNATIONAL_ERROR',
      message: error.message,
      details: error.details,
    },
    metadata: {
      timestamp: new Date().toISOString(),
      correlationId,
      version: '42.0.0',
    },
  };
};

