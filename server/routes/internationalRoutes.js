/*
 * @route   GET /api/v1/international/jurisdictions
 * @desc    List all supported jurisdictions with profiles
 * @access  Premium
 * @tier    PREMIUM
 */
router.get(
  '/jurisdictions',
  tieredRateLimiter('premium'),
  cacheMiddleware({ ttl: CACHE_TTL.JURISDICTIONS }),
  async (req, res, next) => {
    const startTime = performance.now();
    const correlationId = req.correlationId;
    const { id: tenantId } = req.tenantContext;
    const userId = req.user?.id;

    try {
      const analyzer = getAnalyzer();
      const jurisdictions = analyzer.getSupportedJurisdictions();

      // Group by legal family for better organization
      const families = await analyzer.getLegalFamilies();

      const response = {
        count: jurisdictions.length,
        jurisdictions,
        families,
        metadata: {
          totalJurisdictions: jurisdictions.length,
          legalFamilies: Object.keys(families).length,
        },
      };

      // Track billing (premium tier)
      const charge = await trackBilling(
        tenantId,
        userId,
        'list_jurisdictions',
        INTERNATIONAL_TIERS.PREMIUM,
        {
          jurisdictionCount: jurisdictions.length,
        }
      );

      // Forensic logging
      await QuantumLogger.logAction(tenantId, userId, 'INTL_JURISDICTIONS_LIST', null, {
        count: jurisdictions.length,
        families: Object.keys(families),
        charge,
        tier: INTERNATIONAL_TIERS.PREMIUM,
      });

      const duration = performance.now() - startTime;
      metrics.timing('international.jurisdictions.duration', duration);

      res.json(
        formatSuccess(response, {
          self: req.originalUrl,
          processingTimeMs: Math.round(duration),
          correlationId,
          tier: INTERNATIONAL_TIERS.PREMIUM,
          charge,
        })
      );
    } catch (error) {
      trackError('international', error.code || 'jurisdictions_error');
      next(new AppError(error.message, 500, 'JURISDICTIONS_FETCH_FAILED'));
    }
  }
);

